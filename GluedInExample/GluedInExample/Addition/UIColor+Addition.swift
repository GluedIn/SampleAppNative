import Foundation
import UIKit

extension UIColor {
    
    class var themeColor: UIColor {
        return #colorLiteral(red: 0, green: 0.2, blue: 1, alpha: 1)
    }
    
    class var primaryThemeColor: UIColor {
        return #colorLiteral(red: 0.2039215686, green: 0.2039215686, blue: 0.2039215686, alpha: 1)
    }
}
